    public  void add_cart(String n,String q, String p){
        int total = Integer.parseInt(q)*Integer.parseInt(p);
        String t=Integer.toString(total);
        PreparedStatement ps;
        String query = "INSERT INTO `cart_list`(`u_id`, `food_name`, `quantity`, `price`, `total`) VALUES (?,?,?,?,?)";
            
            try {
                ps = (PreparedStatement) MyConnection.getConnection().prepareStatement(query);
                ps.setString(1, id.getText());
                ps.setString(2, n);
                ps.setString(3, q);
                ps.setString(4, p);
                ps.setString(5, t);
                
                if(ps.executeUpdate() > 0)
                {
                    Icon icon=new javax.swing.ImageIcon(getClass().getResource("/icons/success.png"));
        JOptionPane.showMessageDialog(null,"Food name: "+n+"\nPrice: "+p+"\nQuantity: "+q+"\nTotal = "+t, "!!Added to cart successfully!!", JOptionPane.INFORMATION_MESSAGE,icon);
                }
                else{
                    JOptionPane.showMessageDialog(null,"Somthing went wrong", "Error",2);
                }
                } catch (SQLException ex) {
                    Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
                }
    }

public void update_carttable(){
        try {
            PreparedStatement ps;
        ResultSet rs;
        String U_id = ep_uid.getText();
        
        String query = "SELECT * FROM `cart_list` WHERE `u_id` =?";
        
            ps = (PreparedStatement) MyConnection.getConnection().prepareStatement(query);
            ps.setString(1, U_id);
            
            rs = ps.executeQuery();
            DefaultTableModel tm=(DefaultTableModel)cartTable.getModel();
            tm.setRowCount(0);
            int i=1;
            while(rs.next())
            {
                    Object o[]={i,rs.getString(3),rs.getString(1),rs.getString(4),rs.getString(5),rs.getString(6),rs.getString(7)};
                    tm.addRow(o);
                    i++;
            }
                jLabel_total.setText("");
                int sum=0;
                for(int j=0;j<cartTable.getRowCount();j++){
                    sum+=Integer.parseInt(cartTable.getValueAt(j, 6).toString());
                }
                jLabel_TOTAL.setText(Integer.toString(sum));
            
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public  boolean valid_Card(String card){
        if(jTextField_MM.getText().isEmpty()){
            return false;
        }
        else if(jTextField_YY.getText().isEmpty()){
            return false;
        }
        else if(jTextField_CVV.getText().isEmpty()){
            return false;
        }
        else{
        int mm=Integer.parseInt(jTextField_MM.getText().toString());
        int yy=Integer.parseInt(jTextField_YY.getText().toString());
        int cvv=Integer.parseInt(jTextField_CVV.getText().toString());
        String Card= "^[0-9]{16}";
        Pattern cardPat=Pattern.compile(Card,Pattern.CASE_INSENSITIVE);
        Matcher mat=cardPat.matcher(card);
        if(mat.find()&&(mm<=31&&mm>0)&&(yy<=65&&yy>0)&&(cvv<=999&&cvv>0)){
            return true;
        }
        else{
        return false;
        }
        }
    }
    public boolean validUPI(String upi){
        String upiFormat= "^[a-zA-Z0-9.\\-_]{2,256}@[a-zA-Z]{2,64}$";
        Pattern upiPat=Pattern.compile(upiFormat);
        Matcher mat=upiPat.matcher(upi);
        return mat.find();
    }
    
